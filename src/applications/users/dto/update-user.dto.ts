import { IsOptional, IsString, IsEnum } from 'class-validator';
import { UserRole } from '../../shared/constants/user-role';

export class UpdateUserDto {
  @IsOptional()
  @IsString()
  name?: string;

  @IsOptional()
  @IsEnum(UserRole)
  role?: UserRole;

  @IsOptional()
  @IsString()
  phone?: string;

  @IsOptional()
  @IsString()
  position?: string;

  @IsOptional()
  @IsString()
  documentId?: string;
}
